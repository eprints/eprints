sub checktype_filename 
{
  # reads in 16k of selected file, or returns undef if can't open,
  # then checks contents

  my ( $filename ) = @_;
  my $fh = IO::File->new($filename) || return undef;
  my $data;
  $fh->read($data, 16*1024);
  $fh->close;
  return checktype_contents($data);
}


