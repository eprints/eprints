    return q{text/html};
  }
  if ($data =~ m[^\<TITLE]) {
    return q{text/html};
  }
  if ($data =~ m[^\<title]) {
    return q{text/html};
  }
  if ($data =~ m[^\<html]) {
    return q{text/html};
  }
  if ($data =~ m[^\<HTML]) {
    return q{text/html};
  }

#seb add on
  if ($data =~ m[^\<\?xml]) {
      return q{text/xml};
  }

  if ($data =~ m[^\<\?XML]) {
      return q{text/xml};
  }
#end seb add on

  if ($data =~ m[^\<\!SQ\ A\/E\>]) {
    return q{A/E SGML Document binary};
  }
  if ($data =~ m[^\<\!SQ\ STS\>]) {
    return q{A/E SGML binary styles file};
  }
  if ($data =~ m[^\367\203]) {
    return q{font/x-tex};
  }
  if ($data =~ m[^\367\131]) {
    return q{font/x-tex};
  }
  if ($data =~ m[^\367\312]) {
    return q{font/x-tex};
  }
  if (length $data > 2) {
    $substr = substr($data, 2, 1024);
    if (defined $substr && $substr =~ m[^\000\021]) {
      return q{font/x-tex-tfm};
    }
  }
  if (length $data > 2) {
    $substr = substr($data, 2, 1024);
    if (defined $substr && $substr =~ m[^\000\022]) {
      return q{font/x-tex-tfm};
    }
  }
  if (length $data > 1) {
    $substr = substr($data, 1, 1024);
    if (defined $substr && $substr =~ m[^WPC]) {
      return q{(Corel/WP)};
    }
  }
  if ($data =~ m[^BLENDER]) {
    return q{application/x-blender};
  }

  # autogerated code ends

  # fallback case
  {
    return 'application/octet-stream';
  }
}

